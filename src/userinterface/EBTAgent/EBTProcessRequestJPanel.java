    private void SendToFinanceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendToFinanceButtonActionPerformed

        String message = messageText.getText();
        if (message.equals("")) {
            JOptionPane.showMessageDialog(null, "Message is mandatory!");
            return;
        } else {
            insuranceWorkRequest.setProcessMessage(message);
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to proceed?");

            if (dialogResult == JOptionPane.YES_OPTION) {
                Organization org = null;
                for (Organization organization : enterprise.getOrgDir().getOrganizations()) {
                    if (organization instanceof OrgEBTFinance) {
                        org = organization;
                        break;
                    }
                }

                if (org != null) {
                    org.getProcessQueue().getProcesReq().add(insuranceWorkRequest);
                    account.getProcessQueue().getProcesReq().add(insuranceWorkRequest);
                }

                JOptionPane.showMessageDialog(null, "Request Approved and Sent To Finance Department");
                insuranceWorkRequest.setProcessStatus("Sent To Finance Department");
                insuranceWorkRequest.setEbtAgent(account.getEmploye().getEmpName());
                insuranceWorkRequest.setRcvr(null);

                messageText.setText("");
                rejectButton.setEnabled(false);
                SendToFinanceButton.setEnabled(false);

            }
            messageText.setText("");
        }
    }//GEN-LAST:event_SendToFinanceButtonActionPerformed

