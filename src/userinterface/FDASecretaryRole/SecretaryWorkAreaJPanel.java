    private void assignToMeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignToMeButtonActionPerformed
        int selectedRow = processRequestTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first from the table to view details");
            return;
        } else {
            ProcessRequest req = (FDAApprovalRequest) processRequestTable.getValueAt(selectedRow, 0);
            if (req.getProcessStatus().equals("Sent to Secretary")) {
                req.setRcvr(usrAcnt);
                req.setProcessStatus("Pending on " + req.getRcvr().getEmploye().getEmpName());
                ppltTbl();
                JOptionPane.showMessageDialog(null, "Success !! Request is assigned to you ");
            } else {
                JOptionPane.showMessageDialog(null, "Can't assign this work request, as the work request is in " + req.getProcessStatus() + " status", "Warning!", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_assignToMeButtonActionPerformed

    private void processRequestButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processRequestButtonActionPerformed
        // TODO add your handling code here:

        int selectedRow = processRequestTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first from the table to view details");
            return;
        } else {
            FDAApprovalRequest fundReq = (FDAApprovalRequest) processRequestTable.getValueAt(selectedRow, 0);
            if (fundReq.getProcessStatus().equals("Rejected")) {
                JOptionPane.showMessageDialog(null, "Cannot process a Rejected Request", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            if (fundReq.getProcessStatus().equalsIgnoreCase("Sent to Manager")) {
                JOptionPane.showMessageDialog(null, "Request already processed" , "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (fundReq.getProcessStatus().equalsIgnoreCase("Sent to Secretary")) {
                JOptionPane.showMessageDialog(null, "Please assign selected request first");
                return;
            }
             if(!usrAcnt.equals(fundReq.getRcvr())){
             JOptionPane.showMessageDialog(null, "Not Authorized", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (!usrAcnt.getEmploye().equals(fundReq.getRcvr().getEmploye())) {
                JOptionPane.showMessageDialog(null, "Request assigned to other Officer", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            
            SecretaryWorkRequestJPanel secPrcWrkreqJPanel = new SecretaryWorkRequestJPanel(jPanel, usrAcnt, fundReq, entrpz);
            jPanel.add("secretaryProcessWorkRequestJPanel", secPrcWrkreqJPanel);
            CardLayout layout = (CardLayout) jPanel.getLayout();
            layout.next(jPanel);

        }
    }//GEN-LAST:event_processRequestButtonActionPerformed

    public void ppltTbl() {
        DefaultTableModel model = (DefaultTableModel) processRequestTable.getModel();

        model.setRowCount(0);

        for (ProcessRequest req : secOrg.getProcessQueue().getProcesReq()) {
            String status = req.getProcessStatus();
            Object[] row = new Object[6];
            row[0] = ((FDAApprovalRequest) req);
            row[1] = req.getSndr().getEmploye().getEmpName();
            if (status.equalsIgnoreCase("Sent to Manager") || status.equalsIgnoreCase("Sent to Secretary")) {
                row[2] = null;
            } else {
                row[2] = req.getRcvr() == null ? null : req.getRcvr().getEmploye().getEmpName();
            }
            row[3] = status;
            row[4] = ((FDAApprovalRequest) req).getRestaurantZipCode();
            row[5] = ((FDAApprovalRequest) req).getProcessMessage();

            model.addRow(row);
        }
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        processRequestTable.setRowSorter(sorter);
    }

